DIM2_EXECUTION_EFFICIENCY = """
### 维度二：执行效率 (Execution Efficiency)

> 评估战术行动序列是否精简、无冗余、时序最优。评估对象为行动模式本身的效率（步骤编排、并行度、路径选择），而非特定资源数量的利用率。**通用化战术不绑定机器人数量，不应以"角色负载均衡"为标准**——同一功能角色在执行阶段可由一个或多个机器人承担，角色间切换也是执行阶段的调度决策。

**评分 Checklist（逐项核查）**：

| 编号 | 核查项 | 核查方法 |
|------|--------|---------|
| E1 | **无冗余步骤** | 行动序列中是否存在可合并的相邻步骤？（如两次对同一方向的侦察可合并为一次；对同一目标的连续两次短点射可合并为一次长点射）相邻步骤去重检查 |
| E2 | **无无效等待** | 是否存在一个角色长时间等待而另一角色在执行与前者无依赖关系的步骤？如果有，该等待是否有明确的战术理由（如等待同步信号、等待侦察结果才能决策下一步）？时间线分析 |
| E3 | **并行度合理** | 独立可并行执行的行动是否被错误地串行化？（如两个方向的警戒可同时部署却写成了先后执行）不应并行的行动（如需要前一步侦察结果作为突入条件）是否被错误地并行化？依赖关系分析 |
| E4 | **路径无冗余绕行** | 机动路径是否避免了不必要的绕行？是否避免了穿越已肃清区域的冗余往返？路径效率分析 |
| E5 | **火力经济性** | 火力强度（单发精准射击/短点射/长点射/持续压制）是否与目标期望效果匹配？是否避免了"对单个疑似目标使用持续压制"的过度火力或"对集群威胁使用单发射击"的火力不足？火力-目标匹配检查 |
| E6 | **阶段衔接流畅** | Phase 之间的过渡是否有明确的完成条件和触发信号？是否避免了不必要的 Phase 间停滞？前一阶段的输出（如"走廊已肃清"）是否直接作为下一阶段的输入条件？阶段转换检查 |

**评分锚点**：

| 分数 | 判定标准 |
|------|---------|
| **9-10** | E1-E6 全部通过。并行度安排最优，无冗余步骤。火力与目标精确匹配。阶段过渡条件明确且可检测。可视为该战术模式下的步骤数下界 |
| **7-8** | E1-E5 通过。并行度基本合理，可能存在 1-2 处可优化点（如本可并行的两步骤被串行），但不影响整体时间效率。无明显的步骤冗余或火力浪费 |
| **5-6** | 存在 1-2 处明显冗余（如对同一区域重复侦察且中间无情况变化），或存在不合理串行等待。步骤数超出合理范围约 20%。火力强度存在 1-2 处不匹配 |
| **3-4** | 存在多处冗余步骤和无效等待。行动序列明显过长，并行机会被系统性忽略。火力使用存在明显浪费（对低价值目标分配持续压制火力） |
| **0-2** | 行动序列逻辑混乱，步骤缺乏合理的先后顺序。存在大量自相矛盾的时序安排。即使完成所有步骤也无法达成战术目标 |
"""

