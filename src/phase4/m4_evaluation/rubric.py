DIM3_COMPREHENSION = """
### 维度三：可理解性 (Comprehension)

> 评估战术描述（特别是 Intent 字段）是否精确、清晰、无歧义，使得 VLA（Vision-Language-Action）模型或人类操作员可以准确理解并执行。本维度聚焦于**语言表达的精确性**，而非战术内容的质量。

**评分 Checklist（逐项核查）**：

| 编号 | 核查项 | 核查方法 |
|------|--------|---------|
| C1 | **动作动词精确** | 每个步骤的核心动作是否使用了精确的战术动作动词？（"抵近"、"跃进"、"压制"、"侦察"、"肃清"、"占领"、"破门"、"交替掩护"）而非模糊动词（"处理"、"应对"、"搞定"、"弄一下"）动词精确性检查 |
| C2 | **目标对象明确** | 每个动作针对的目标对象是否明确？（"转角后方区域"、"房门入口方向"、"走廊中段"）而非模糊指向（"前方"、"那边"、"往里"）对象明确性检查 |
| C3 | **执行条件清晰** | 有条件触发（如"确认安全后"、"听到突破声后"）的动作，其触发条件是否明确且可被机器人传感器检测？触发条件可行性检查 |
| C4 | **角色职责无歧义** | 每个步骤的执行主体（功能角色）是否明确且唯一？如果某步骤涉及多角色协同，各自的职责划分是否清楚、不重叠、不遗漏？角色归属检查 |
| C5 | **动作粒度一致** | 同一战术内各步骤的动作粒度是否保持一致？不应出现"步骤1：抵近转角 → 步骤2：占领整个楼层"这种粒度跳跃。粒度一致性检查 |
| C6 | **成功标准可判断** | 每个关键步骤是否隐式或显式给出了成功完成的可判断标准？（如"确认走廊无威胁"——是否说明了确认方式？视觉确认？火力侦察？传感器扫描？）完成标准检查 |
| C7 | **异常处理提示** | 关键高风险动作（突入、跨越开阔地）是否有简短的异常处理提示？（如"若突入受阻，则退至最近掩体建立火力封锁"）异常分支检查 |

**评分锚点**：

| 分数 | 判定标准 |
|------|---------|
| **9-10** | C1-C7 全部通过。每个步骤的意图可在 3 秒内被人类和 VLA 模型准确理解。所有触发条件可被传感器直接验证。关键高风险动作均包含异常分支。协同时各角色职责一目了然 |
| **7-8** | C1-C6 通过。动作动词精确，目标对象明确，角色归属清晰，粒度一致。可能存在 1-2 处"成功标准不够具体"的情况（如"确认安全"但未说明确认方式），但不影响核心执行逻辑 |
| **5-6** | 主体动作可理解，但存在 2-3 处模糊表述（如"适当距离"、"必要时"），或存在步骤间粒度不一致（1-2 处跳变）。角色切换处有暂时歧义但可推断 |
| **3-4** | 多个步骤的意图需要读者自行推断才能理解。触发条件模糊或不可被传感器检测（如"当感觉敌人放松警惕时"）。角色职责在协同步骤中混淆。粒度从原子动作跳变到宏观阶段 |
| **0-2** | 大量步骤无法确定其战术意图。动词严重模糊（大量使用"处理"、"应对"），目标对象缺失。即使人类军事专家也无法准确理解执行方式。协同时谁做什么完全不清楚 |
"""

